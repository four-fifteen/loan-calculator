import { methodLabel, type RepaymentMethod } from "@/lib/loan-calculator";

function eokLabel(eok: number): string {
  if (Number.isInteger(eok)) return `${eok}억`;
  const int = Math.floor(eok);
  const frac = Math.round((eok - int) * 10);
  if (int === 0) return `${frac}천만`;
  return `${int}억 ${frac}천만`;
}

